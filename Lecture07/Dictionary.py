# Creating a dict using '{}'
student = {"name": "Alice", "age": 25, "grade": "A"}
print(student)

# Use 'dict()' with keyword arguments
student1 = dict(name="Alice", age = 25, grade="A")
print(student1)

# Use 'dict()' with a list of tuples
student3 = dict([("name", "Alice"), ("age", 25), ("grade", "A")])
print(student3)